import java.util.*;
public class IanVisitsMary {
    static Scanner sc = new Scanner(System.in);

    public static void solve() {
        long a = sc.nextLong();
        long b = sc.nextLong();

        System.out.println(2);
        System.out.println((a - 1) + " " + 1);
        System.out.println(a + " " + b);
    }

    public static void main(String[] args) {
        int T = sc.nextInt();
        while (T-- > 0) {
            solve();
        }
    }
}
